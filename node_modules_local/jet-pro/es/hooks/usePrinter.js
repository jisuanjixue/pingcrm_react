import { useCreation } from 'ahooks';
import React, { useRef } from 'react';
export default (function () {
  var factory = useCreation(function () {
    return {
      key: ''.concat(new Date().getTime()),
    };
  }, []);
  var refIFrame = useRef();
  var onPrint = function onPrint(content) {
    if (!refIFrame.current) return;
    var iframeWindow = refIFrame.current.contentWindow;
    if (!iframeWindow) {
      return;
    }
    var iframeDocument = iframeWindow.document;
    iframeDocument.body.innerHTML = content || '';
    iframeWindow.print();
  };
  return [
    onPrint,
    /*#__PURE__*/ React.createElement('iframe', {
      key: factory.key,
      style: {
        display: 'none',
      },
      ref: function ref(elm) {
        refIFrame.current = elm;
      },
    }),
  ];
});
