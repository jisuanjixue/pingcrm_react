/**
 * 根据缩放计算响应式配置
 * @param scale 缩放（默认为1）
 * @returns
 */
export var getColProps = function getColProps(scale) {
  var defaultColProps = {
    xs: 24,
    // <576px
    sm: 12,
    // ≥576px
    md: 8,
    // ≥768px
    lg: 8,
    // ≥992px
    xl: 6,
    // ≥1200px
    xxl: 6, // ≥1600px
  };

  if (!scale || scale === 1) return defaultColProps;
  var colProps = {};
  Object.keys(defaultColProps).forEach(function (key) {
    colProps[key] = Math.round(Number(defaultColProps[key]) * scale);
  });
  return colProps;
};
export var getFormItemProps = function getFormItemProps(labelScale) {
  var col = 24;
  var labelCol = Math.round((labelScale || 0.3) * col);
  var wrapperCol = col - labelCol;
  return {
    labelCol: {
      span: labelCol,
    },
    wrapperCol: {
      span: wrapperCol,
    },
  };
};
